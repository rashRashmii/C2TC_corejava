package edu.atria.java.arrays;

public class Student {
	int age;
	String name;
	
	//constructors
	public Student(int age, String name) {
		super();
		this.age = age;
		this.name = name;
	}
	
	//getters and setters 
	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

	
	
	

}